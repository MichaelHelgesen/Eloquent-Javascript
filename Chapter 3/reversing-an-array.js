
// take an array as argument
// create a new array
// return the new array with the values from the one passed in in reverse.
const reverseArray = (array) => {
    let reverseArray = [];
    for (i = array.length-1; i >= 0; i--) {
        reverseArray.push(array[i]);
    }
    return reverseArray;
}

